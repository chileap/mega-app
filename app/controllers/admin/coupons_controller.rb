module Admin
  class CouponsController < Admin::ApplicationController
    def import
      @coupons = Coupons::ImportService.call
      if @coupons.any?
        redirect_to admin_coupons_path, notice: "Coupons imported."
      else
        redirect_to admin_coupons_path, alert: "No coupons imported."
      end
    end

